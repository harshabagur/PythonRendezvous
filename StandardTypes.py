# Learn standard types List, Tuples and Dictionary

print ("Example of List")
sampleList = ["first", 25, 3.4]

print (sampleList[0])
print (sampleList[1])
print (sampleList[2])


print (sampleList[-1])
print (sampleList[-2])
print (sampleList[-3])

#################################################

print ("Example of Tuple")
sampleTuple = ("first", 25, 3.4)

print (sampleTuple[0])
print (sampleTuple[1])
print (sampleTuple[2])


print (sampleTuple[-1])
print (sampleTuple[-2])
print (sampleTuple[-3])

###############################################

print ("Example of Dictionary")

sampleDict = {'Key1':"Delhi", 'Key2': "Bangalore", 'Key3':"Mumbai"}

print ("Key1:", sampleDict['Key1'])
print ("Key2:", sampleDict['Key2'])
print ("Key3:", sampleDict['Key3'])
